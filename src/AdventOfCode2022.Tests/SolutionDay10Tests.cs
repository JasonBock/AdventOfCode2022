    public static void GetCPUForSmallInstructionSet() 
    {
        var instructions = new[]
        {
            "noop",
            "addx 3",
            "addx -5",
        };

        var cpu = new CPU(instructions);

        Assert.Equal(5, cpu.Cycle);
        Assert.Equal(-1, cpu.XRegister);
        Assert.Equal(0, cpu.SignalStrength);
    }

